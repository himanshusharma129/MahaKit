package inspire2connect.inspire2connect;

public class audio_improve {
    String url;
    int story_number;

    public audio_improve() {
    }

    public audio_improve(String url, int story_number) {
        this.url = url;
        this.story_number = story_number;
    }

    public String getUrl() {
        return url;
    }

    public int getStory_number() {
        return story_number;
    }
}
